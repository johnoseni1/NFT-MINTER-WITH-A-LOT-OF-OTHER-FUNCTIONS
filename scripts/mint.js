const HDWalletProvider = require("truffle-hdwallet-provider")
const web3 = require('web3')
const INFURA_KEY = process.env.INFURA_API_KEY
const CONTRACT_ADDRESS = '0x7cFf9aA171626068E1F68543E54eA8DC8a708007' // deployInfo.ERC1155Address
const OWNER_ADDRESS = '0x79E153d86589186759Dcc004065dBb5d966f368A' // deployInfo.ownerAddress
const NETWORK = 'rinkeby' // deployInfo.network


let MNEMONIC; 
let RPC_URL;

if (NETWORK == 'ganache')
{
    MNEMONIC = process.env.GANACHE_MNEMONIC
    RPC_URL = "http://localhost:8545/";
}

else
{
    MNEMONIC = process.env.TESTNET_MNEMONIC
    RPC_URL = `https://${NETWORK}.infura.io/v3/${INFURA_KEY}`;
}

if (!MNEMONIC || !INFURA_KEY || !OWNER_ADDRESS || !NETWORK) {
    console.error("Please set a mnemonic, infura key, owner, network, and contract address.")
    return
}

async function main() {
    const provider = new HDWalletProvider(MNEMONIC, RPC_URL);
    const web3Instance = new web3(provider);

    if (!CONTRACT_ADDRESS) {
        console.error("Please set an NFT contract address.")
        return
    }

    const myCollectible = new web3Instance.eth.Contract(CONTRACT_ABI, CONTRACT_ADDRESS, { gasLimit: "1000000" })
    const INIT_SUPPLY = 1;

    for (var i = 0; i < 100; i++) {
        let result = await myCollectible.methods.create(OWNER_ADDRESS, INIT_SUPPLY, "", []).send({ from: OWNER_ADDRESS });
  
          // assert that user has the appropriate number of tokens
        let ownerBalance = await myCollectible.methods.balanceOf(OWNER_ADDRESS, i).call();       
        console.log(ownerBalance + " of id:" + i + " created. Transaction: " + result.transactionHash)
      }
      console.log("-------------------")
      console.log("Mint Finished, press Ctrl+C to exit.")        
}

